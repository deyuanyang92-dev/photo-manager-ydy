from app.utils import net_proxy


# ── helpers ─────────────────────────────────────────────────────────────────

class _FakeProxyHandler(http.server.BaseHTTPRequestHandler):
    """Answers 200 to any GET — stands in for a Clash HTTP proxy."""

    def do_GET(self):  # noqa: N802
        body = b"\x89PNG fake"
        self.send_response(200)
        self.send_header("Content-Length", str(len(body)))
        self.end_headers()
        self.wfile.write(body)

    def log_message(self, *a):  # silence
        pass


@pytest.fixture()
def fake_proxy():
    srv = http.server.HTTPServer(("127.0.0.1", 0), _FakeProxyHandler)
    th = threading.Thread(target=srv.serve_forever, daemon=True)
    th.start()
    yield "127.0.0.1", srv.server_address[1]
    srv.shutdown()


