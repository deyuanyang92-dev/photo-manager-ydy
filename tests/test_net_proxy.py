"""test_net_proxy.py — TDD tests for OSM-scoped proxy auto-detection.

openstreetmap.org is unreachable by direct connection from mainland-China
networks. The GUI (launched from a desktop shell with no http_proxy env) must
auto-discover a local Clash-style proxy and use it for OSM requests ONLY —
never globally, or LAN collab sync would be routed into the proxy too.

No real network: probes are tested against a local fake proxy HTTP server.
"""
from __future__ import annotations

